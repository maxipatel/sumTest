package rest.gradle;

public class Data {
	private final String ok;
    private final Object result;
    
	public Data(String ok, Object result) {
		super();
		this.ok = ok;
		this.result = result;
	}

	public String getOk() {
		return ok;
	}

	public Object getResult() {
		return result;
	}
   
	
    
}
